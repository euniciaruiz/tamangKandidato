				Ang Tamaka.org ay online resource para matulungan ang botante na makapili ng Tamang Kandidato para Senador ngayong halalan sa Mayo 13, 2019. Gamitin ito para i-compare ang mga piling kandidato para Senador base sa ilang mahahalagang isyu ng lipunan. Layon ng Tamaka.org na matulungan ang botante na makilatis ang mga kandidato kung sila ba ay:
