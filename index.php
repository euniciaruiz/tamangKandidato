					<b><li>MakaDIYOS</li></b>
					<li><b>MakaTAO;</b> o</li>
					<b><li>MakaBAYAN</li></b>
				</ul></p>
				<p>Ang 36 na kandidatong Senador na tampok dito ay pinili base sa tatlong criteria:
				<ol>
				<li>resulta ng national surveys;</li> 
				<li>pagiging bahagi ng major political party/coalition;</li> 
				<li>may nakuhang impormasyon sa internet (as of March 31, 2019)</li>
